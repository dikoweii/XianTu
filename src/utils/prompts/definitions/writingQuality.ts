export const FORBIDDEN_WORDS = `
# 禁用词汇与表达

## 1. 绝望化表达（导致文本过于消极）
**禁止使用：**
- 麻木、绝望、灭顶、面无表情
- 无力感、疲惫感、自我厌弃、自暴自弃、破罐子破摔
- 倦意、无力、脱力、疲惫、虚弱、虚脱
- 抽干全身力气

**替代方案：**
- 用具体的生理反应：呼吸变缓、眼神涣散、肩膀下垂
- 用动作描写：靠在墙上、闭上眼睛、深吸一口气

## 2. 机械化表达（导致文本失去人性）
**禁止使用：**
- 机械、机械式的、冰冷、冰凉
- 手术刀、解剖、精密、仪器
- 展品、雕塑、教具

**替代方案：**
- 用自然的动作：平静地看着、缓缓转身、轻声说道
- 用情感描写：心中一沉、眉头微皱、嘴角抽动

## 3. 八股化表达（陈词滥调）
**禁止使用：**
- 像是一根(xxx的)针、像是一把(xxx的)刀
- 石子/石头/湖面/涟漪（常见八股比喻）
- 一道惊雷、电流/闪电
- 深入骨髓、如释重负、如蒙大赦
- 前所未来、难以言喻、潮水/淹没/席卷

**替代方案：**
- 用具体的感受：胸口发闷、手心出汗、后背发凉
- 用新鲜的比喻：结合修仙世界的元素创造独特比喻

## 4. 过度修饰词（导致夸张）
**禁止使用：**
- 猛地、瞬间、"轰"地一下
- 过度、强烈、剧烈、极度、深深
- 震惊、惊慌、激动、紧张、紧绷、绷紧、突然、死死地
- 狂热、残酷、残忍、冷酷
- 荒唐、荒诞、荒谬

**替代方案：**
- 删除过度修饰，用动作本身说话
- 用程度递进：先是一愣 → 眉头皱起 → 脸色变了

## 5. 过度身体描写（导致色情化/物化）
**禁止使用：**
- 睫毛、紧闭、滴出血来
- 锁骨、尾椎骨、喉结、修长、骨节分明
- 指尖、指节、xxx的手
- 血色、血压、上涨、血色尽褪、通红
- 热辣辣、火辣辣、滚烫、发烫、滚热、热、烫、燃烧

**替代方案：**
- 用整体动作：抬起手、转过身、低下头
- 用表情变化：脸色微变、眼神闪烁、嘴角上扬

## 6. 过度生理反应（导致夸张）
**禁止使用：**
- 脊背（用"后背/背部"）
- 握拳、握紧、攥紧、攥住
- 颤抖、颤音
- 直冲、直窜
- 牙缝、紧咬、咬着
- 长长地呼出一口气

**替代方案：**
- 用简单动作：手指微动、呼吸一滞、眼神一凝

## 7. 过度理性化（导致冷漠）
**禁止使用：**
- 冷静、审视、审判
- 不易察觉（模糊量词）
- 僵硬、僵住、一片空白

**替代方案：**
- 用具体观察：看了一眼、沉默片刻、微微点头

## 8. 其他八股表达
**禁止使用：**
- 打圈、打转、打着圈、打着转
- 狡黠、挤了挤眼睛、眨了眨眼
- 乐了（用"笑了笑"）
- 宗教化：狂热、信徒、仪式、献祭、祭品、宗教、信仰

**替代方案：**
- 用自然对话和动作推进情节
`.trim()

export const WRITING_QUALITY_STANDARDS = `
# 文本质量标准

## 1. 自然性原则
- ✅ 用日常动作：走、看、说、想
- ✅ 用简单描写：天色渐暗、风吹过、人声渐远
- ❌ 避免过度修饰：不用"猛地"、"瞬间"、"突然"

## 2. 具体性原则
- ✅ 用具体细节：他拿起茶杯，茶水已经凉了
- ✅ 用环境衬托：【远处传来钟声，提醒着时辰已晚】
- ❌ 避免抽象感受：不用"无力感"、"绝望感"

## 3. 温度感原则
- ✅ 保持人物的情感温度：犹豫、思考、微笑、叹息
- ✅ 用对话推进情节："你怎么看？"他问道
- ❌ 避免机械化：不用"冰冷"、"机械"、"手术刀"

## 4. 新鲜感原则
- ✅ 创造独特比喻：结合修仙世界元素
- ✅ 用场景特色：宗门、法阵、灵气、修炼
- ❌ 避免陈词滥调：不用"石子入湖"、"一道惊雷"

## 5. 节制性原则
- ✅ 适度描写：一两个细节即可
- ✅ 留白艺术：不必事事说透
- ❌ 避免过度：不堆砌形容词，不过度描写身体部位

## 检查清单
在生成文本后，自查以下问题：
1. 是否使用了禁用词汇？→ 删除或替换
2. 是否过度修饰？→ 简化表达
3. 是否机械化/绝望化？→ 增加人性温度
4. 是否陈词滥调？→ 创造新鲜表达
5. 是否过度描写身体？→ 改用整体动作
`.trim()

